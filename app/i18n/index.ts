function getByPath(obj: unknown, path: string): string | undefined {
  const parts = path.split('.');
  let current: any = obj;
  for (const p of parts) {
    if (!current || typeof current !== 'object' || !(p in current)) {
      return undefined;
    }
    current = current[p];
  }
  return typeof current === 'string' ? current : undefined;
}

export function normalizeLanguage(raw?: string | null): UILanguage {
  if (!raw) return 'es';
  const lower = raw.toLowerCase();
  if (lower.startsWith('en')) return 'en';
  return 'es';
}

export function detectSystemLanguage(): UILanguage {
  try {
    const langs =
      (typeof navigator !== 'undefined' && (navigator.languages?.length ? navigator.languages : [navigator.language])) ||
      [];
    const preferred = langs.find(Boolean) || 'es';
    return normalizeLanguage(preferred);
  } catch {
    return 'es';
  }
}

export function getStoredLanguage(): UILanguage | null {
  try {
    const raw = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (!raw) return null;
    return normalizeLanguage(raw);
  } catch {
    return null;
  }
}

export function persistLanguage(language: UILanguage): void {
  try {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  } catch {
    // no-op
  }
}

export function getInitialLanguage(): UILanguage {
  return getStoredLanguage() ?? detectSystemLanguage();
}

export function t(language: UILanguage, key: string, vars?: Record<string, string | number>): string {
  const primary = getByPath(translations[language], key);
  const fallback = getByPath(translations.es, key) ?? key;
  const template = primary ?? fallback;
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (acc, [k, v]) => acc.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v)),
    template,
  );
}
